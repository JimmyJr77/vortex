import PDFDocument from 'pdfkit'

/**
 * Build a parent progress report PDF buffer.
 * @param {{ athleteName: string; narrative: string; prs?: Array<{ label: string; value: string }>; goals?: Array<{ title: string; targetDate?: string | null }>; generatedAt?: string }} data
 */
export function buildProgressReportPdf(data) {
  const {
    athleteName,
    narrative,
    prs = [],
    goals = [],
    generatedAt = new Date().toLocaleDateString(),
  } = data

  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ margin: 54, size: 'LETTER' })
    const chunks = []
    doc.on('data', (chunk) => chunks.push(chunk))
    doc.on('end', () => resolve(Buffer.concat(chunks)))
    doc.on('error', reject)

    doc.fontSize(22).fillColor('#111111').text('Vortex Athletics', { align: 'center' })
    doc.fontSize(14).fillColor('#666666').text('Athlete Progress Report', { align: 'center' })
    doc.moveDown(1.5)

    doc.fontSize(11).fillColor('#333333')
    doc.text(`Athlete: ${athleteName}`)
    doc.text(`Report date: ${generatedAt}`)
    doc.moveDown()

    doc.fontSize(13).fillColor('#111111').text('Coach Summary')
    doc.moveDown(0.3)
    doc.fontSize(11).fillColor('#333333').text(narrative || 'No summary available.', { lineGap: 4 })
    doc.moveDown()

    if (prs.length > 0) {
      doc.fontSize(13).fillColor('#111111').text('Recent Personal Records')
      doc.moveDown(0.3)
      doc.fontSize(11).fillColor('#333333')
      for (const pr of prs) {
        doc.text(`• ${pr.label}: ${pr.value}`)
      }
      doc.moveDown()
    }

    if (goals.length > 0) {
      doc.fontSize(13).fillColor('#111111').text('Active Goals')
      doc.moveDown(0.3)
      doc.fontSize(11).fillColor('#333333')
      for (const g of goals) {
        const due = g.targetDate ? ` (target: ${g.targetDate})` : ''
        doc.text(`• ${g.title}${due}`)
      }
      doc.moveDown()
    }

    doc.fontSize(9).fillColor('#999999').text(
      'Generated by Vortex Coaching Corner. Share with families as a progress snapshot.',
      { align: 'center' },
    )

    doc.end()
  })
}
