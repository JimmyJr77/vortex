#!/usr/bin/env node
/**
 * Merge researched YouTube links into coaching.exercise media_library — batch 3.
 *
 * Reads enriched sources for parts 1, 6, 7, 9:
 *   docs/exercise-youtube-links-1.md
 *   docs/exercise-youtube-links-6.md
 *   docs/exercise-youtube-links-7.json
 *   docs/exercise-youtube-links-9.json
 *
 * Writes backend/migrations/204_coaching_exercise_youtube_links_batch3.sql
 *
 *   node scripts/generate-204-exercise-youtube-links.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import {
  buildSlugMap,
  buildYoutubeUpdate,
  resolveExerciseSlug,
} from './lib/exercise-youtube-migration.mjs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const docsDir = path.join(root, 'docs')
const migrationPath = path.join(root, 'backend/migrations/204_coaching_exercise_youtube_links_batch3.sql')

const SOURCES = [
  { file: 'exercise-youtube-links-1.md', kind: 'markdown' },
  { file: 'exercise-youtube-links-6.md', kind: 'markdown' },
  { file: 'exercise-youtube-links-7.json', kind: 'json' },
  { file: 'exercise-youtube-links-9.json', kind: 'json' },
]

function parseMarkdownBatch(filePath) {
  const text = fs.readFileSync(filePath, 'utf8')
  const match = text.match(/```json\n([\s\S]*?)\n```/)
  if (!match) throw new Error(`No JSON block in ${filePath}`)
  return JSON.parse(match[1])
}

function parseSource({ file, kind }) {
  const filePath = path.join(docsDir, file)
  if (!fs.existsSync(filePath)) throw new Error(`Missing source file: ${filePath}`)
  if (kind === 'markdown') return parseMarkdownBatch(filePath)
  return JSON.parse(fs.readFileSync(filePath, 'utf8'))
}

const slugByName = buildSlugMap(root)
const byKey = new Map()
const unresolved = []

for (const source of SOURCES) {
  const batch = parseSource(source)
  for (const entry of batch) {
    const links = (entry.youtube_links ?? []).filter(Boolean)
    if (links.length === 0) continue
    const slug = resolveExerciseSlug(entry, slugByName)
    const key = slug ?? `name:${entry.name}`
    if (!slug) unresolved.push(entry.name)
    byKey.set(key, { slug, name: entry.name, youtube_links: links })
  }
}

const updates = [...byKey.values()]
  .sort((a, b) => (a.slug ?? a.name).localeCompare(b.slug ?? b.name))
  .map(buildYoutubeUpdate)
  .filter(Boolean)

const header = `-- Exercise YouTube reference links — batch 3 (parts 1, 6, 7, 9).
-- IDEMPOTENT. Generated by scripts/generate-204-exercise-youtube-links.mjs
-- Matches by exercise slug (portable across environments). Falls back to name when slug unknown.
-- Merges embeddable watch URLs into media_library.clinical_or_sport_science_references.

`

fs.writeFileSync(migrationPath, header + updates.join('\n') + '\n')
console.log(`Wrote ${updates.length} exercise updates to ${migrationPath}`)
if (unresolved.length) {
  console.warn(`Warning: ${unresolved.length} exercises matched by name only (no slug in catalog):`)
  console.warn(unresolved.slice(0, 10).join(', '), unresolved.length > 10 ? '...' : '')
}
