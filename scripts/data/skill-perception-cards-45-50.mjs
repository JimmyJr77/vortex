const SUBROLE = 'perception_action_reactive_movement'
const FAMILY = 'Reactive movement'

function mapIntensity(ceiling) {
  if (ceiling === 'moderate_to_high') return 'high'
  if (ceiling === 'low_to_moderate') return 'moderate'
  return ceiling ?? 'moderate'
}

function perceptionCard({
  slug,
  name,
  slot,
  cardSummary,
  coachLanguage,
  athleteLanguage,
  description,
  whyItWorks,
  whyItGoesHere,
  bestPlacement,
  commonMisuse,
  scalingGuidance,
  tenets,
  methodologies,
  physiology,
  patterns,
  equipment,
  body_regions,
  movementRequirements,
  coachingExecution,
  dosage,
  phaseProfile,
  scaling,
  pairsWellBefore = [],
  pairsWellAfter = [],
  avoidBefore = [],
  doNotUseWhen = [],
  goodForSessions = ['general_warmup', 'sprint_prep', 'landing_prep'],
  mediaReferences = [],
  mediaInternalNotes = [],
  setupRequirements = [],
}) {
  const req = { ...movementRequirements }
  if (setupRequirements.length > 0) {
    req.setup_requirements = setupRequirements
  }
  return {
    slug,
    name,
    family: FAMILY,
    slot,
    subrole: SUBROLE,
    cardSummary,
    coachLanguage,
    athleteLanguage,
    description,
    whyItWorks,
    whyItGoesHere,
    bestPlacement,
    commonMisuse,
    scalingGuidance,
    genderSpecificNotes: GENDER_DEFAULT,
    tenets,
    methodologies,
    physiology,
    patterns,
    equipment,
    body_regions,
    movementRequirements: req,
    coachingExecution,
    dosage,
    phaseProfile: {
      role: 'primary',
      daily_ok: true,
      freshness_required: true,
      ...phaseProfile,
      intensity_ceiling: mapIntensity(phaseProfile.intensity_ceiling),
    },
    scaling,
    pairsWellBefore,
    pairsWellAfter,
    avoidBefore,
    doNotUseWhen,
    goodForSessions,
    mediaReferences,
    mediaInternalNotes,
  }
}

