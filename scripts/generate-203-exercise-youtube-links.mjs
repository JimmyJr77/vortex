#!/usr/bin/env node
/**
 * Merge researched YouTube links into coaching.exercise media_library.
 *
 * Reads enriched markdown batches from docs/exercise-youtube-links-{2,3,4,5,8,10,11}.md
 * and writes backend/migrations/203_coaching_exercise_youtube_links_batch2.sql
 *
 *   node scripts/generate-203-exercise-youtube-links.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const docsDir = path.join(root, 'docs')
const migrationPath = path.join(root, 'backend/migrations/203_coaching_exercise_youtube_links_batch2.sql')

const PART_FILES = [
  'exercise-youtube-links-2.md',
  'exercise-youtube-links-3.md',
  'exercise-youtube-links-4.md',
  'exercise-youtube-links-5.md',
  'exercise-youtube-links-8.md',
  'exercise-youtube-links-10.md',
  'exercise-youtube-links-11.md',
]

function parseMarkdownBatch(filePath) {
  const text = fs.readFileSync(filePath, 'utf8')
  const match = text.match(/```json\n([\s\S]*?)\n```/)
  if (!match) throw new Error(`No JSON block in ${filePath}`)
  return JSON.parse(match[1])
}

function sqlString(value) {
  return `'${String(value).replace(/'/g, "''")}'`
}

function buildUpdate(exercise) {
  const links = (exercise.youtube_links ?? []).filter(Boolean)
  if (links.length === 0) return null

  const arrayLiteral = `ARRAY[${links.map(sqlString).join(', ')}]::text[]`

  return `-- ${exercise.name} (${exercise.id})
UPDATE coaching.exercise e
SET
  media_library = jsonb_set(
    COALESCE(e.media_library, '{}'::jsonb),
    '{clinical_or_sport_science_references}',
    COALESCE(
      (
        SELECT jsonb_agg(elem ORDER BY elem)
        FROM (
          SELECT DISTINCT elem
          FROM (
            SELECT jsonb_array_elements_text(
              COALESCE(e.media_library->'clinical_or_sport_science_references', '[]'::jsonb)
            ) AS elem
            UNION ALL
            SELECT unnest(${arrayLiteral}) AS elem
          ) combined
          WHERE elem IS NOT NULL AND btrim(elem) <> ''
        ) deduped
      ),
      '[]'::jsonb
    ),
    true
  ),
  updated_at = now()
WHERE e.id = ${exercise.id};
`
}

const entries = []
for (const file of PART_FILES) {
  const filePath = path.join(docsDir, file)
  if (!fs.existsSync(filePath)) {
    console.warn(`Skipping missing file: ${file}`)
    continue
  }
  const batch = parseMarkdownBatch(filePath)
  entries.push(...batch)
}

const byId = new Map()
for (const entry of entries) {
  if (!entry?.id) continue
  const links = (entry.youtube_links ?? []).filter(Boolean)
  if (links.length === 0) continue
  byId.set(entry.id, { id: entry.id, name: entry.name, youtube_links: links })
}

const updates = [...byId.values()]
  .sort((a, b) => a.id - b.id)
  .map(buildUpdate)
  .filter(Boolean)

const header = `-- Exercise YouTube reference links — batch 2 (parts 2, 3, 4, 5, 8, 10, 11).
-- IDEMPOTENT. Generated by scripts/generate-203-exercise-youtube-links.mjs
-- Merges researched watch URLs into media_library.clinical_or_sport_science_references
-- without removing existing non-YouTube references.

`

fs.writeFileSync(migrationPath, header + updates.join('\n') + '\n')
console.log(`Wrote ${updates.length} exercise updates to ${migrationPath}`)
