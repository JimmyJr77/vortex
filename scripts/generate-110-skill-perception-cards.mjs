/**
 * Generates backend/migrations/110_coaching_skill_perception_cards.sql
 * Run: node scripts/generate-110-skill-perception-cards.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { SKILL_PERCEPTION_CARDS, SKILL_PERCEPTION_SLUGS } from './data/skill-perception-cards-45-50.mjs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))

function sqlStr(s) {
  return `'${String(s ?? '').replace(/'/g, "''")}'`
}

function jsonb(obj) {
  return `'${JSON.stringify(obj).replace(/'/g, "''")}'::jsonb`
}

function sqlBool(v) {
  return v ? 'TRUE' : 'FALSE'
}

const FACET_TABLE = {
  tenet: 'tenet',
  methodology: 'methodology',
  physiology: 'physiological_emphasis',
  pattern: 'movement_pattern',
  equipment: 'equipment',
  body_regions: 'body_region',
}

const FACET_TAG_FIELD = {
  tenet: 'tenets',
  methodology: 'methodologies',
  physiology: 'physiology',
  pattern: 'patterns',
  equipment: 'equipment',
  body_regions: 'body_regions',
}

const COHORT_KEYS = [
  'youth_beginner', 'youth_intermediate', 'teen', 'adult_beginner',
  'adult_advanced', 'older_adult', 'pregnancy_postpartum',
]

const PERCEPTION_SLOT_RESEQUENCE = [
  ['mirror_reaction', 280],
  ['visual_reaction', 281],
  ['visual_decision', 282],
  ['object_reaction', 283],
  ['movement_game', 284],
  ['reactive_start', 285],
]

const VALIDATION_EDU = {
  title: 'Perception-Action / Reactive Movement without chaos',
  short_summary:
    'Reactive drills should sharpen see-decide-move quality at low-to-moderate intensity — not become conditioning games, max sprints, or unsafe partner contact in Skill.',
  what_it_is:
    'Perception cluster cards teach partner mirroring, coach visual cues, color decisions, ball drops, bounded tag games, and gate reactions while athletes are fresh.',
  why_it_matters:
    'Reactive skill under fatigue or poor spacing transfers into guessing, collisions, and sloppy first steps. Skill teaches perception-action; Output expresses it fast; Fitness repeats it under load.',
  programming_guidance:
    'Keep mirror/tag rounds ≤12–15s, require lateral shuffle prep before mirror work, cap ball/gate reps with full rest, use no-contact shadow for beginners, and move timed max sprints to Output.',
  common_misuse:
    'Do not assign reactive work after HIIT, allow diving in ball drop, run long tag rounds, use crossing gate layouts, or skip deceleration prerequisites — reduce dose or change phase intent.',
}

let sql = `-- Movement Intelligence perception cards 45–50 (rich card v2 content).
-- IDEMPOTENT. Generated by scripts/generate-110-skill-perception-cards.mjs
-- UPDATE-only on slugs from migration 105. Fine slots seeded in migration 104.

-- Resequence perception / reactive movement slots to band 280–285 (after balance 260–268)
`

for (const [slotKey, orderIndex] of PERCEPTION_SLOT_RESEQUENCE) {
  sql += `UPDATE coaching.phase_order_slot pos SET order_index = ${orderIndex}
FROM coaching.session_phase sp
WHERE sp.key = 'movement_intelligence' AND pos.phase_id = sp.id AND pos.key = '${slotKey}';

`
}

sql += `-- Equipment taxonomy for perception cluster tags
INSERT INTO coaching.equipment (key, name, sort_order) VALUES
  ('tennis_ball', 'Tennis Ball', 21),
  ('reaction_ball', 'Reaction Ball', 22)
ON CONFLICT (key) DO UPDATE SET name = EXCLUDED.name, sort_order = EXCLUDED.sort_order;

`

for (const card of SKILL_PERCEPTION_CARDS) {
  const pairing = {
    pairs_well_before: card.pairsWellBefore ?? [],
    pairs_well_after: card.pairsWellAfter ?? [],
    good_for_sessions: card.goodForSessions ?? [],
    avoid_before: card.avoidBefore ?? [],
    avoid_after: [],
    do_not_use_when: card.doNotUseWhen ?? [],
  }
  const media = {
    demo_video_sources: [],
    coaching_articles: [],
    clinical_or_sport_science_references: card.mediaReferences ?? [],
    internal_notes: card.mediaInternalNotes ?? [],
  }
  const pp = card.phaseProfile
  const d = card.dosage
  const sessionVolMin = d.session_volume_min != null ? Number(d.session_volume_min) : 'NULL'
  const sessionVolMax = d.session_volume_max != null ? Number(d.session_volume_max) : 'NULL'
  const defaultDistance =
    d.default_distance_yards != null
      ? Number(d.default_distance_yards)
      : d.default_distance != null && /^\d+$/.test(String(d.default_distance))
        ? Number(d.default_distance)
        : 'NULL'

  sql += `-- ${card.name} (${card.slug})
UPDATE coaching.exercise SET
  name = ${sqlStr(card.name)},
  description = ${sqlStr(card.description)},
  card_summary = ${sqlStr(card.cardSummary)},
  coach_language = ${sqlStr(card.coachLanguage)},
  athlete_language = ${sqlStr(card.athleteLanguage)},
  movement_family = ${sqlStr(card.family)},
  primary_phase_key = 'movement_intelligence',
  phase_subrole = ${sqlStr(card.subrole)},
  primary_order_slot = ${sqlStr(card.slot)},
  default_sets = ${d.default_sets},
  default_reps = ${d.default_reps ?? 'NULL'},
  default_work_seconds = ${d.default_work_seconds ?? 'NULL'},
  default_rest_seconds = ${d.default_rest_seconds},
  est_seconds_per_set = ${d.est_seconds_per_set},
  movement_requirements = ${jsonb(card.movementRequirements)},
  coaching_execution = ${jsonb(card.coachingExecution)},
  pairing_logic = ${jsonb(pairing)},
  media_library = ${jsonb(media)},
  updated_at = now()
WHERE slug = ${sqlStr(card.slug)};

UPDATE coaching.exercise_phase_profile p SET
  role = ${sqlStr(pp.role ?? 'primary')},
  order_slot = ${sqlStr(card.slot)},
  order_index = pos.order_index,
  fit_weight = ${pp.fit_weight},
  freshness_required = ${sqlBool(pp.freshness_required)},
  fatigue_sensitivity = ${pp.fatigue_sensitivity},
  fatigue_cost = ${pp.fatigue_cost},
  technical_complexity = ${pp.technical_complexity},
  impact_level = ${pp.impact_level},
  intensity_ceiling = ${sqlStr(pp.intensity_ceiling)},
  notes = ${sqlStr(pp.notes)}
FROM coaching.exercise e
JOIN coaching.session_phase sp ON sp.key = 'movement_intelligence'
JOIN coaching.phase_order_slot pos ON pos.key = ${sqlStr(card.slot)} AND pos.phase_id = sp.id
WHERE p.exercise_id = e.id AND p.phase_id = sp.id AND e.slug = ${sqlStr(card.slug)};

UPDATE coaching.exercise_dosage_profile d SET
  volume_unit = ${sqlStr(d.volume_unit)},
  default_sets = ${d.default_sets},
  default_reps = ${d.default_reps ?? 'NULL'},
  default_distance = ${defaultDistance},
  default_work_seconds = ${d.default_work_seconds ?? 'NULL'},
  default_rest_seconds = ${d.default_rest_seconds},
  est_seconds_per_set = ${d.est_seconds_per_set},
  default_rpe_min = ${d.default_rpe_min},
  default_rpe_max = ${d.default_rpe_max},
  session_volume_min = ${sessionVolMin},
  session_volume_max = ${sessionVolMax}
FROM coaching.exercise e
WHERE d.exercise_id = e.id AND d.profile_name = 'Default' AND e.slug = ${sqlStr(card.slug)};

UPDATE coaching.exercise_regimen_rule r SET
  can_be_daily = ${sqlBool(pp.daily_ok !== false)},
  weekly_max_frequency = CASE WHEN ${sqlBool(pp.daily_ok !== false)} THEN 7 ELSE r.weekly_max_frequency END
FROM coaching.exercise e
WHERE r.exercise_id = e.id AND e.slug = ${sqlStr(card.slug)};

`
}

sql += `-- Refresh weighted taxonomy tags for perception cards
DELETE FROM coaching.exercise_tag t
USING coaching.exercise e
WHERE t.exercise_id = e.id
  AND e.slug IN (${SKILL_PERCEPTION_SLUGS.map(sqlStr).join(', ')})
  AND t.facet_type IN ('tenet', 'methodology', 'physiology', 'pattern', 'equipment', 'body_region');

`

for (const [facetKey, table] of Object.entries(FACET_TABLE)) {
  const facetType = facetKey === 'body_regions' ? 'body_region' : facetKey
  const tagField = FACET_TAG_FIELD[facetKey]
  const rows = []
  for (const card of SKILL_PERCEPTION_CARDS) {
    const tags = card[tagField] ?? []
    for (const t of tags) {
      rows.push(`  (${sqlStr(card.slug)}, ${sqlStr(t.key)}, ${t.weight})`)
    }
  }
  if (rows.length === 0) continue
  sql += `INSERT INTO coaching.exercise_tag (exercise_id, facet_type, facet_id, weight)
SELECT e.id, '${facetType}', f.id, v.weight
FROM (VALUES\n`
  sql += rows.join(',\n')
  sql += `
) AS v(slug, fkey, weight)
JOIN coaching.exercise e ON e.slug = v.slug
JOIN coaching.${table} f ON f.key = v.fkey
ON CONFLICT (exercise_id, facet_type, facet_id) DO UPDATE SET weight = EXCLUDED.weight;

`
}

for (const ck of COHORT_KEYS) {
  sql += `UPDATE coaching.exercise_scaling_profile sp SET
  load_guidance = v.guidance,
  gender_specific_notes = CASE WHEN '${ck}' = 'adult_beginner' THEN v.gender_notes ELSE sp.gender_specific_notes END
FROM (VALUES\n`
  sql += SKILL_PERCEPTION_CARDS.map((c) => {
    const guidance = c.scaling[ck] ?? c.scaling.adult_beginner ?? 'Warm-up dose only — prioritize quality.'
    return `  (${sqlStr(c.slug)}, ${sqlStr(guidance)}, ${sqlStr(c.genderSpecificNotes)})`
  }).join(',\n')
  sql += `
) AS v(slug, guidance, gender_notes)
JOIN coaching.exercise e ON e.slug = v.slug
WHERE sp.exercise_id = e.id AND sp.cohort_key = '${ck}';

INSERT INTO coaching.exercise_scaling_profile (exercise_id, cohort_key, label, scale_direction, load_guidance, gender_specific_notes)
SELECT e.id, '${ck}', '${ck.replace(/_/g, ' ')}', 'baseline', v.guidance,
  CASE WHEN '${ck}' = 'adult_beginner' THEN v.gender_notes ELSE NULL END
FROM (VALUES\n`
  sql += SKILL_PERCEPTION_CARDS.map((c) => {
    const guidance = c.scaling[ck] ?? c.scaling.adult_beginner ?? 'Warm-up dose only — prioritize quality.'
    return `  (${sqlStr(c.slug)}, ${sqlStr(guidance)}, ${sqlStr(c.genderSpecificNotes)})`
  }).join(',\n')
  sql += `
) AS v(slug, guidance, gender_notes)
JOIN coaching.exercise e ON e.slug = v.slug
WHERE NOT EXISTS (
  SELECT 1 FROM coaching.exercise_scaling_profile sp
  WHERE sp.exercise_id = e.id AND sp.cohort_key = '${ck}'
);

`
}

sql += `-- Why-layer education
INSERT INTO coaching.education_content (
  entity_type, entity_key, entity_id, title, short_summary,
  what_it_is, why_it_works, why_it_matters, why_it_goes_here,
  programming_guidance, common_misuse, scaling_guidance
)
SELECT
  'exercise', e.slug, e.id, e.name, e.card_summary,
  e.description,
  v.why_works,
  'Develops tagged tenets and physiological qualities for athletic development.',
  v.why_here,
  v.best_placement,
  v.misuse,
  v.scale_guidance
FROM (VALUES\n`
sql += SKILL_PERCEPTION_CARDS.map((c) =>
  `  (${sqlStr(c.slug)}, ${sqlStr(c.whyItWorks)}, ${sqlStr(c.whyItGoesHere)}, ${sqlStr(c.bestPlacement)}, ${sqlStr(c.commonMisuse)}, ${sqlStr(c.scalingGuidance)})`,
).join(',\n')
sql += `
) AS v(slug, why_works, why_here, best_placement, misuse, scale_guidance)
JOIN coaching.exercise e ON e.slug = v.slug
ON CONFLICT (entity_type, entity_key, entity_id) DO UPDATE SET
  title = EXCLUDED.title,
  short_summary = EXCLUDED.short_summary,
  what_it_is = EXCLUDED.what_it_is,
  why_it_works = EXCLUDED.why_it_works,
  why_it_goes_here = EXCLUDED.why_it_goes_here,
  programming_guidance = EXCLUDED.programming_guidance,
  common_misuse = EXCLUDED.common_misuse,
  scaling_guidance = EXCLUDED.scaling_guidance,
  updated_at = now();

-- Perception-cluster validation education (completes 50-card Skill library)
INSERT INTO coaching.education_content (
  entity_type, entity_key, entity_id, title, short_summary,
  what_it_is, why_it_matters, programming_guidance, common_misuse
)
VALUES (
  'validation_rule',
  'skill_perception_readiness',
  NULL,
  ${sqlStr(VALIDATION_EDU.title)},
  ${sqlStr(VALIDATION_EDU.short_summary)},
  ${sqlStr(VALIDATION_EDU.what_it_is)},
  ${sqlStr(VALIDATION_EDU.why_it_matters)},
  ${sqlStr(VALIDATION_EDU.programming_guidance)},
  ${sqlStr(VALIDATION_EDU.common_misuse)}
)
ON CONFLICT (entity_type, entity_key, entity_id) DO UPDATE SET
  title = EXCLUDED.title,
  short_summary = EXCLUDED.short_summary,
  what_it_is = EXCLUDED.what_it_is,
  why_it_matters = EXCLUDED.why_it_matters,
  programming_guidance = EXCLUDED.programming_guidance,
  common_misuse = EXCLUDED.common_misuse,
  updated_at = now();
`

const outPath = path.join(__dirname, '../backend/migrations/110_coaching_skill_perception_cards.sql')
fs.writeFileSync(outPath, sql)
console.log('Wrote', outPath, '—', SKILL_PERCEPTION_CARDS.length, 'skill perception cards')
