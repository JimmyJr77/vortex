/**
 * Generates backend/migrations/129_coaching_control_resilience_seed.sql
 * Run: node scripts/generate-129-control-seed.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { CONTROL_MOVEMENTS } from './data/control-movements-top-50.mjs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const PRIMARY_PHASE = 'control_resilience'
const MOVEMENTS = CONTROL_MOVEMENTS
const cohortKeys = ['youth_beginner', 'youth_intermediate', 'teen', 'adult_beginner', 'adult_advanced', 'older_adult', 'pregnancy_postpartum']

const SUBROLE_WHY = {
  landing_braking_control: 'Belongs in Landing & Braking Control — absorb force, stick landings, and organize knee/hip/ankle alignment.',
  single_leg_balance_foot_ankle_hip_control: 'Belongs in Single-Leg Balance / Foot-Ankle-Hip Control — stance quality, foot tripod, and hip/pelvis ownership.',
  trunk_pelvis_anti_movement_control: 'Belongs in Trunk / Pelvis / Anti-Movement Control — anti-extension, anti-rotation, and rib/pelvis stiffness.',
  scapular_wrist_hand_support_resilience: 'Belongs in Scapular / Wrist / Hand-Support Resilience — shoulder-blade, wrist, and hand-support durability.',
  slow_eccentric_isometric_joint_resilience: 'Belongs in Slow Eccentric / Isometric Joint Resilience — low-speed joint and tissue tolerance through holds and slow eccentrics.',
}

function sqlStr(s) {
  return `'${String(s).replace(/'/g, "''")}'`
}

function jsonReq(m) {
  const balance = m.subrole.includes('single_leg') || m.slot.includes('single_leg') || m.slot.includes('balance') ? 'single_leg' : 'stable'
  const obj = {
    primary_joint_actions: m.joints,
    primary_tissues: [],
    breathing_demand: 'nasal',
    balance_demand: balance,
    impact_level: m.impact,
  }
  return `'${JSON.stringify(obj).replace(/'/g, "''")}'::jsonb`
}

function jsonExec(m) {
  const obj = {
    movement_description: m.desc,
    setup: m.setup,
    execution_steps: m.steps,
    coach_cues: m.cues,
    common_faults: m.faults,
  }
  return `'${JSON.stringify(obj).replace(/'/g, "''")}'::jsonb`
}

function slugList(arr) {
  return arr.map((m) => sqlStr(m.slug)).join(', ')
}

function coachLangText(m) {
  return `Use in Control / Resilience (${m.subrole.replace(/_/g, ' ')}) after Output and usually after main Capacity. Precision and alignment — not Fitness circuits or max-strength loading.`
}

function athleteLangText() {
  return 'Move slow, stay aligned, and own each position. Quality and control matter more than speed or fatigue.'
}

let sql = `-- Seed 50 Control / Resilience phase movements (card v2 + subrole hierarchy).
-- IDEMPOTENT. Generated by scripts/generate-129-control-seed.mjs

INSERT INTO coaching.exercise (
  facility_id, name, slug, description, sport_id, skill_level, age_min,
  default_sets, default_reps, default_work_seconds, default_rest_seconds, est_seconds_per_set,
  is_published, visibility,
  card_summary, coach_language, athlete_language,
  movement_family, primary_phase_key, phase_subrole, primary_order_slot,
  movement_requirements, coaching_execution
)
SELECT
  (SELECT id FROM public.facility ORDER BY id LIMIT 1),
  d.name, d.slug, d.description,
  (SELECT id FROM coaching.sport WHERE key = 'fitness'),
  d.skill::public.skill_level,
  d.age_min,
  d.sets, d.reps, d.work, d.rest, d.est,
  TRUE, 'facility',
  d.summary, d.coach_lang, d.athlete_lang,
  d.family, '${PRIMARY_PHASE}', d.subrole, d.slot,
  d.req::jsonb, d.exec::jsonb
FROM (VALUES
`

sql += MOVEMENTS.map((m) => {
  const summary = `${m.focus} — precision control and joint resilience work.`
  const reps = m.reps ?? null
  const work = m.work ?? null
  return `  (${sqlStr(m.name)}, ${sqlStr(m.slug)}, ${sqlStr(m.desc)}, ${sqlStr(m.skill)}, ${m.ageMin}, ${m.sets}, ${reps ?? 'NULL'}, ${work ?? 'NULL'}, ${m.rest}, ${m.est}, ${sqlStr(summary)}, ${sqlStr(coachLangText(m))}, ${sqlStr(athleteLangText())}, ${sqlStr(m.family)}, ${sqlStr(m.subrole)}, ${sqlStr(m.slot)}, ${jsonReq(m).replace('::jsonb', '')}::jsonb, ${jsonExec(m).replace('::jsonb', '')}::jsonb)`
}).join(',\n')

sql += `
) AS d(name, slug, description, skill, age_min, sets, reps, work, rest, est, summary, coach_lang, athlete_lang, family, subrole, slot, req, exec)
ON CONFLICT (facility_id, slug) DO NOTHING;

`

for (const facet of ['tenet', 'methodology', 'physiology', 'pattern', 'equipment', 'body_region']) {
  const tableMap = { tenet: 'tenet', methodology: 'methodology', physiology: 'physiological_emphasis', pattern: 'movement_pattern', equipment: 'equipment', body_region: 'body_region' }
  const keyField = facet === 'tenet' ? 'tenets' : facet === 'methodology' ? 'methods' : facet === 'physiology' ? 'phys' : facet === 'pattern' ? 'pattern' : facet === 'equipment' ? 'equip' : 'body'
  sql += `INSERT INTO coaching.exercise_tag (exercise_id, facet_type, facet_id, weight)\nSELECT e.id, '${facet}', f.id, 4\nFROM (VALUES\n`
  const rows = []
  for (const m of MOVEMENTS) {
    if (facet === 'pattern' || facet === 'equipment' || facet === 'body_region') {
      rows.push(`  (${sqlStr(m.slug)}, ${sqlStr(m[keyField])})`)
    } else {
      for (const k of m[keyField] || []) {
        rows.push(`  (${sqlStr(m.slug)}, ${sqlStr(k)})`)
      }
    }
  }
  sql += rows.join(',\n')
  sql += `\n) AS v(slug, fkey)\nJOIN coaching.exercise e ON e.slug = v.slug\nJOIN coaching.${tableMap[facet]} f ON f.key = v.fkey\nON CONFLICT (exercise_id, facet_type, facet_id) DO NOTHING;\n\n`
}

sql += `INSERT INTO coaching.exercise_phase_profile (
  exercise_id, phase_id, fit_weight, role, order_slot, order_index,
  freshness_required, fatigue_sensitivity, fatigue_cost, technical_complexity, impact_level, intensity_ceiling
)
SELECT e.id, sp.id, 5, 'primary', m.slot, pos.order_index,
  FALSE, 2, 2, 2, m.impact, 'moderate'
FROM (VALUES\n`
sql += MOVEMENTS.map((m) => `  (${sqlStr(m.slug)}, ${sqlStr(m.slot)}, ${m.impact})`).join(',\n')
sql += `
) AS m(slug, slot, impact)
JOIN coaching.exercise e ON e.slug = m.slug
JOIN coaching.session_phase sp ON sp.key = 'control_resilience'
JOIN coaching.phase_order_slot pos ON pos.key = m.slot AND pos.phase_id = sp.id
ON CONFLICT (exercise_id, phase_id) DO UPDATE SET
  role = EXCLUDED.role, order_slot = EXCLUDED.order_slot, fit_weight = EXCLUDED.fit_weight,
  freshness_required = EXCLUDED.freshness_required, fatigue_sensitivity = EXCLUDED.fatigue_sensitivity,
  impact_level = EXCLUDED.impact_level, intensity_ceiling = EXCLUDED.intensity_ceiling;

`

sql += `INSERT INTO coaching.exercise_dosage_profile (
  exercise_id, profile_name, is_default, volume_unit, default_sets, default_reps,
  default_work_seconds, default_rest_seconds, default_distance, est_seconds_per_set, default_rpe_min, default_rpe_max
)
SELECT e.id, 'Default', TRUE, m.unit, m.sets, m.reps, m.work, m.rest, m.distance, m.est, 4, 7
FROM (VALUES\n`
sql += MOVEMENTS.map((m) => `  (${sqlStr(m.slug)}, ${sqlStr(m.unit)}, ${m.sets}, ${m.reps ?? 'NULL'}, ${m.work ?? 'NULL'}, ${m.rest}, ${m.distance ?? 'NULL'}, ${m.est})`).join(',\n')
sql += `
) AS m(slug, unit, sets, reps, work, rest, distance, est)
JOIN coaching.exercise e ON e.slug = m.slug
WHERE NOT EXISTS (SELECT 1 FROM coaching.exercise_dosage_profile d WHERE d.exercise_id = e.id AND d.profile_name = 'Default');

`

sql += `INSERT INTO coaching.exercise_safety_profile (
  exercise_id, risk_level, impact_level, requires_coach_supervision, readiness_checks
)
SELECT e.id, CASE WHEN m.impact >= 2 THEN 2 ELSE 1 END, COALESCE(m.impact, 0),
  CASE WHEN m.subrole IN ('landing_braking_control', 'scapular_wrist_hand_support_resilience') OR m.slot IN ('perturbation_balance', 'handstand_line_control', 'handstand_eccentric_control') THEN 'recommended' ELSE 'none' END,
  ARRAY['No acute joint pain', 'Supervised for youth and handstand/perturbation work', 'Stop on alignment breakdown or sharp pain']
FROM (VALUES\n`
sql += MOVEMENTS.map((m) => `  (${sqlStr(m.slug)}, ${m.impact ?? 0}, ${sqlStr(m.subrole)}, ${sqlStr(m.slot)})`).join(',\n')
sql += `
) AS m(slug, impact, subrole, slot)
JOIN coaching.exercise e ON e.slug = m.slug
WHERE NOT EXISTS (SELECT 1 FROM coaching.exercise_safety_profile s WHERE s.exercise_id = e.id);

`

sql += `INSERT INTO coaching.exercise_regimen_rule (
  exercise_id, can_be_daily, weekly_max_frequency, minimum_hours_between_hard_exposures, counts_as_neural, counts_as_high_intensity
)
SELECT e.id,
  m.daily_ok,
  CASE WHEN m.daily_ok THEN 7 WHEN m.subrole = 'slow_eccentric_isometric_joint_resilience' THEN 3 ELSE 4 END,
  CASE WHEN m.slot IN ('hamstring_isometric_resilience', 'hamstring_eccentric_control', 'handstand_line_control') THEN 72 ELSE 24 END,
  FALSE, FALSE
FROM (VALUES\n`
sql += MOVEMENTS.map((m) => `  (${sqlStr(m.slug)}, ${m.dailyOk ? 'TRUE' : 'FALSE'}, ${sqlStr(m.subrole)}, ${sqlStr(m.slot)})`).join(',\n')
sql += `
) AS m(slug, daily_ok, subrole, slot)
JOIN coaching.exercise e ON e.slug = m.slug
WHERE NOT EXISTS (SELECT 1 FROM coaching.exercise_regimen_rule r WHERE r.exercise_id = e.id);

UPDATE coaching.exercise_regimen_rule r SET
  can_be_daily = m.daily_ok,
  weekly_max_frequency = CASE WHEN m.daily_ok THEN 7 WHEN m.subrole = 'slow_eccentric_isometric_joint_resilience' THEN 3 ELSE 4 END
FROM (VALUES\n`
sql += MOVEMENTS.map((m) => `  (${sqlStr(m.slug)}, ${m.dailyOk ? 'TRUE' : 'FALSE'}, ${sqlStr(m.subrole)})`).join(',\n')
sql += `
) AS m(slug, daily_ok, subrole)
JOIN coaching.exercise e ON e.slug = m.slug
WHERE r.exercise_id = e.id;

`

for (const ck of cohortKeys) {
  sql += `INSERT INTO coaching.exercise_scaling_profile (exercise_id, cohort_key, label, scale_direction, load_guidance, complexity_guidance, coach_notes)
SELECT e.id, '${ck}', '${ck.replace(/_/g, ' ')}', 'baseline', 'Reduce hold time, range, or speed before reducing alignment standard.', 'Use regression variation until positions stay clean.', 'Control dose — precision over fatigue.'
FROM coaching.exercise e
WHERE e.slug IN (${slugList(MOVEMENTS)})
  AND NOT EXISTS (SELECT 1 FROM coaching.exercise_scaling_profile sp WHERE sp.exercise_id = e.id AND sp.cohort_key = '${ck}');

`
}

sql += `INSERT INTO coaching.education_content (
  entity_type, entity_key, entity_id, title, short_summary, what_it_is, why_it_matters,
  why_it_goes_here, programming_guidance, common_misuse, scaling_guidance
)
SELECT
  'exercise', e.slug, e.id, e.name,
  e.card_summary,
  COALESCE(e.description, e.name),
  'Teaches the athlete to organize force safely through position ownership, alignment, and low-speed resilience.',
  CASE e.phase_subrole
    WHEN 'landing_braking_control' THEN ${sqlStr(SUBROLE_WHY.landing_braking_control)}
    WHEN 'single_leg_balance_foot_ankle_hip_control' THEN ${sqlStr(SUBROLE_WHY.single_leg_balance_foot_ankle_hip_control)}
    WHEN 'trunk_pelvis_anti_movement_control' THEN ${sqlStr(SUBROLE_WHY.trunk_pelvis_anti_movement_control)}
    WHEN 'scapular_wrist_hand_support_resilience' THEN ${sqlStr(SUBROLE_WHY.scapular_wrist_hand_support_resilience)}
    WHEN 'slow_eccentric_isometric_joint_resilience' THEN ${sqlStr(SUBROLE_WHY.slow_eccentric_isometric_joint_resilience)}
    ELSE 'Place in Control / Resilience after Output and usually after Capacity — precision before Fitness fatigue.'
  END,
  'Use holds, slow eccentrics, and sticks with full rest. Progress via longer holds, slower tempo, smaller base, or perturbation — not random burnout.',
  'Do not use short-rest high-density circuits and call it Control. Hard resilience work is not daily max-intensity tissue loading.',
  'Youth: supervision on perturbation, handstand, and landing progressions. Scale by alignment quality first.'
FROM coaching.exercise e
WHERE e.slug IN (${slugList(MOVEMENTS)})
ON CONFLICT (entity_type, entity_key, entity_id) DO UPDATE SET
  title = EXCLUDED.title,
  short_summary = EXCLUDED.short_summary,
  what_it_is = EXCLUDED.what_it_is,
  why_it_goes_here = EXCLUDED.why_it_goes_here,
  programming_guidance = EXCLUDED.programming_guidance,
  common_misuse = EXCLUDED.common_misuse,
  scaling_guidance = EXCLUDED.scaling_guidance,
  updated_at = now();

`

const outPath = path.join(__dirname, '../backend/migrations/129_coaching_control_resilience_seed.sql')
fs.writeFileSync(outPath, sql)
console.log('Wrote', outPath, '—', MOVEMENTS.length, 'movements')
