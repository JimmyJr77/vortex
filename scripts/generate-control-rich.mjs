/**
 * Generates Resilience rich card migrations 130–134.
 * Run: node scripts/generate-control-rich.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { CONTROL_LANDING_CARDS, CONTROL_LANDING_SLUGS } from './data/control-landing-cards-1-10.mjs'
import { CONTROL_SINGLE_LEG_CARDS, CONTROL_SINGLE_LEG_SLUGS } from './data/control-single-leg-cards-11-20.mjs'
import { CONTROL_TRUNK_CARDS, CONTROL_TRUNK_SLUGS } from './data/control-trunk-cards-21-30.mjs'
import { CONTROL_SCAPULAR_CARDS, CONTROL_SCAPULAR_SLUGS } from './data/control-scapular-cards-31-40.mjs'
import { CONTROL_SLOW_ECCENTRIC_CARDS, CONTROL_SLOW_ECCENTRIC_SLUGS } from './data/control-slow-eccentric-cards-41-50.mjs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))

const CLUSTERS = [
  { migration: 130, file: '130_coaching_control_landing_cards.sql', label: 'Landing & Braking Control cards 1–10', cards: CONTROL_LANDING_CARDS, slugs: CONTROL_LANDING_SLUGS },
  { migration: 131, file: '131_coaching_control_single_leg_cards.sql', label: 'Single-Leg Balance / Foot-Ankle-Hip Control cards 11–20', cards: CONTROL_SINGLE_LEG_CARDS, slugs: CONTROL_SINGLE_LEG_SLUGS },
  { migration: 132, file: '132_coaching_control_trunk_cards.sql', label: 'Trunk / Pelvis / Anti-Movement Control cards 21–30', cards: CONTROL_TRUNK_CARDS, slugs: CONTROL_TRUNK_SLUGS },
  { migration: 133, file: '133_coaching_control_scapular_cards.sql', label: 'Scapular / Wrist / Hand-Support Resilience cards 31–40', cards: CONTROL_SCAPULAR_CARDS, slugs: CONTROL_SCAPULAR_SLUGS },
  { migration: 134, file: '134_coaching_control_slow_eccentric_cards.sql', label: 'Slow Eccentric / Isometric Joint Resilience cards 41–50', cards: CONTROL_SLOW_ECCENTRIC_CARDS, slugs: CONTROL_SLOW_ECCENTRIC_SLUGS },
]

function sqlStr(s) {
  return `'${String(s ?? '').replace(/'/g, "''")}'`
}

function jsonb(obj) {
  return `'${JSON.stringify(obj).replace(/'/g, "''")}'::jsonb`
}

function sqlBool(v) {
  return v ? 'TRUE' : 'FALSE'
}

function mapIntensityCeiling(ceiling) {
  if (ceiling === 'moderate_to_high' || ceiling === 'high_to_max') return 'high'
  if (ceiling === 'low_to_moderate') return 'moderate'
  return ceiling ?? 'moderate'
}

function movementRequirementsJson(card) {
  const base = { ...(card.movementRequirements ?? {}) }
  if (card.setupRequirements) base.setup_requirements = card.setupRequirements
  if (card.landingControlProfile) base.landing_control_profile = card.landingControlProfile
  if (card.singleLegControlProfile) base.single_leg_control_profile = card.singleLegControlProfile
  if (card.antiMovementProfile) base.anti_movement_profile = card.antiMovementProfile
  if (card.handSupportProfile) base.hand_support_profile = card.handSupportProfile
  if (card.jointResilienceProfile) base.joint_resilience_profile = card.jointResilienceProfile
  return jsonb(base)
}

const FACET_TABLE = {
  tenet: 'tenet',
  methodology: 'methodology',
  physiology: 'physiological_emphasis',
  pattern: 'movement_pattern',
  equipment: 'equipment',
  body_regions: 'body_region',
}

const FACET_TAG_FIELD = {
  tenet: 'tenets',
  methodology: 'methodologies',
  physiology: 'physiology',
  pattern: 'patterns',
  equipment: 'equipment',
  body_regions: 'body_regions',
}

const COHORT_KEYS = [
  'youth_beginner', 'youth_intermediate', 'teen', 'adult_beginner',
  'adult_advanced', 'older_adult', 'pregnancy_postpartum',
]

function generateClusterSql(cluster) {
  const { label, cards, slugs, migration } = cluster
  let sql = `-- Resilience phase ${label} (rich card v2 content).
-- IDEMPOTENT. Generated by scripts/generate-control-rich.mjs
-- UPDATE-only on slugs from migration 129. Fine slots seeded in migration 128.

`

  if (migration === 131) {
    sql += `INSERT INTO coaching.equipment (key, name, sort_order) VALUES
  ('y_balance_kit', 'Y-Balance kit', 95)
ON CONFLICT (key) DO NOTHING;

`
  }

  if (migration === 132) {
    sql += `INSERT INTO coaching.equipment (key, name, sort_order) VALUES
  ('slider', 'Slider', 96)
ON CONFLICT (key) DO NOTHING;

`
  }

  if (migration === 134) {
    sql += `INSERT INTO coaching.equipment (key, name, sort_order) VALUES
  ('yoga_block', 'Yoga Block', 97),
  ('foam_roller', 'Foam Roller', 98),
  ('partner', 'Partner / Manual Anchor', 99)
ON CONFLICT (key) DO NOTHING;

`
  }

  if (migration === 133) {
    sql += `INSERT INTO coaching.equipment (key, name, sort_order) VALUES
  ('parallettes', 'Parallettes / Push-Up Handles', 34),
  ('parallel_bars', 'Parallel Bars / Dip Bars', 35)
ON CONFLICT (key) DO NOTHING;

`
  }

  for (const card of cards) {
    const pairing = {
      pairs_well_before: card.pairsWellBefore ?? [],
      pairs_well_after: card.pairsWellAfter ?? [],
      good_for_sessions: card.goodForSessions ?? [],
      avoid_before: card.avoidBefore ?? [],
      avoid_after: [],
      do_not_use_when: card.doNotUseWhen ?? [],
    }
    const media = {
      demo_video_sources: [],
      coaching_articles: [],
      clinical_or_sport_science_references: card.mediaReferences ?? [],
      internal_notes: card.mediaInternalNotes ?? [],
    }
    const pp = card.phaseProfile
    const d = card.dosage
    const sessionVolMin = d.session_volume_min != null ? String(d.session_volume_min) : 'NULL'
    const sessionVolMax = d.session_volume_max != null ? String(d.session_volume_max) : 'NULL'
    const defaultDistance = d.default_distance != null ? String(d.default_distance) : 'NULL'
    const tempo = d.tempo != null ? sqlStr(String(d.tempo)) : 'NULL'
    const estSeconds = d.est_seconds_per_set != null ? String(d.est_seconds_per_set) : 'NULL'
    const supervisionNote = pp.requires_supervision ? ` Supervision: ${pp.requires_supervision}.` : ''
    const profileNotes = `${pp.notes ?? ''}${supervisionNote}`.trim()

    sql += `-- ${card.name} (${card.slug})
UPDATE coaching.exercise SET
  name = ${sqlStr(card.name)},
  description = ${sqlStr(card.description)},
  card_summary = ${sqlStr(card.cardSummary)},
  coach_language = ${sqlStr(card.coachLanguage)},
  athlete_language = ${sqlStr(card.athleteLanguage)},
  movement_family = ${sqlStr(card.family)},
  primary_phase_key = 'resilience',
  phase_subrole = ${sqlStr(card.subrole)},
  primary_order_slot = ${sqlStr(card.slot)},
  default_sets = ${d.default_sets},
  default_reps = ${d.default_reps ?? 'NULL'},
  default_work_seconds = ${d.default_work_seconds ?? 'NULL'},
  default_rest_seconds = ${d.default_rest_seconds},
  est_seconds_per_set = ${estSeconds},
  movement_requirements = ${movementRequirementsJson(card)},
  coaching_execution = ${jsonb(card.coachingExecution)},
  pairing_logic = ${jsonb(pairing)},
  media_library = ${jsonb(media)},
  updated_at = now()
WHERE slug = ${sqlStr(card.slug)};

UPDATE coaching.exercise_phase_profile p SET
  role = ${sqlStr(pp.role ?? 'primary')},
  order_slot = ${sqlStr(card.slot)},
  order_index = pos.order_index,
  fit_weight = ${pp.fit_weight},
  freshness_required = ${sqlBool(pp.freshness_required !== false)},
  fatigue_sensitivity = ${pp.fatigue_sensitivity},
  fatigue_cost = ${pp.fatigue_cost},
  technical_complexity = ${pp.technical_complexity},
  impact_level = ${pp.impact_level},
  intensity_ceiling = ${sqlStr(mapIntensityCeiling(pp.intensity_ceiling))},
  notes = ${sqlStr(profileNotes)}
FROM coaching.exercise e
JOIN coaching.session_phase sp ON sp.key = 'resilience'
JOIN coaching.phase_order_slot pos ON pos.key = ${sqlStr(card.slot)} AND pos.phase_id = sp.id
WHERE p.exercise_id = e.id AND p.phase_id = sp.id AND e.slug = ${sqlStr(card.slug)};

UPDATE coaching.exercise_dosage_profile d SET
  volume_unit = ${sqlStr(d.volume_unit)},
  default_sets = ${d.default_sets},
  default_reps = ${d.default_reps ?? 'NULL'},
  default_distance = ${defaultDistance},
  default_work_seconds = ${d.default_work_seconds ?? 'NULL'},
  default_rest_seconds = ${d.default_rest_seconds},
  tempo = ${tempo},
  est_seconds_per_set = ${estSeconds},
  default_rpe_min = ${d.default_rpe_min},
  default_rpe_max = ${d.default_rpe_max},
  session_volume_min = ${sessionVolMin},
  session_volume_max = ${sessionVolMax}
FROM coaching.exercise e
WHERE d.exercise_id = e.id AND d.profile_name = 'Default' AND e.slug = ${sqlStr(card.slug)};

UPDATE coaching.exercise_regimen_rule r SET
  can_be_daily = ${sqlBool(pp.daily_ok !== false)},
  weekly_max_frequency = CASE WHEN ${sqlBool(pp.daily_ok !== false)} THEN 7 ELSE r.weekly_max_frequency END
FROM coaching.exercise e
WHERE r.exercise_id = e.id AND e.slug = ${sqlStr(card.slug)};

`
  }

  sql += `DELETE FROM coaching.exercise_tag t
USING coaching.exercise e
WHERE t.exercise_id = e.id
  AND e.slug IN (${slugs.map(sqlStr).join(', ')})
  AND t.facet_type IN ('tenet', 'methodology', 'physiology', 'pattern', 'equipment', 'body_region');

`

  for (const [facetKey, table] of Object.entries(FACET_TABLE)) {
    const facetType = facetKey === 'body_regions' ? 'body_region' : facetKey
    const tagField = FACET_TAG_FIELD[facetKey]
    const rows = []
    for (const card of cards) {
      const tags = card[tagField] ?? []
      for (const t of tags) {
        rows.push(`  (${sqlStr(card.slug)}, ${sqlStr(t.key)}, ${t.weight})`)
      }
    }
    if (rows.length === 0) continue
    sql += `INSERT INTO coaching.exercise_tag (exercise_id, facet_type, facet_id, weight)
SELECT e.id, '${facetType}', f.id, v.weight
FROM (VALUES\n${rows.join(',\n')}
) AS v(slug, fkey, weight)
JOIN coaching.exercise e ON e.slug = v.slug
JOIN coaching.${table} f ON f.key = v.fkey
ON CONFLICT (exercise_id, facet_type, facet_id) DO UPDATE SET weight = EXCLUDED.weight;

`
  }

  for (const ck of COHORT_KEYS) {
    sql += `UPDATE coaching.exercise_scaling_profile sp SET
  load_guidance = v.guidance,
  gender_specific_notes = CASE WHEN '${ck}' = 'adult_beginner' THEN v.gender_notes ELSE sp.gender_specific_notes END
FROM (VALUES\n${cards.map((c) => {
      const guidance = c.scaling[ck] ?? c.scaling.adult_beginner ?? 'Conservative control dose — prioritize alignment and full rest.'
      return `  (${sqlStr(c.slug)}, ${sqlStr(guidance)}, ${sqlStr(c.genderSpecificNotes)})`
    }).join(',\n')}
) AS v(slug, guidance, gender_notes)
JOIN coaching.exercise e ON e.slug = v.slug
WHERE sp.exercise_id = e.id AND sp.cohort_key = '${ck}';

INSERT INTO coaching.exercise_scaling_profile (exercise_id, cohort_key, label, scale_direction, load_guidance, gender_specific_notes)
SELECT e.id, '${ck}', '${ck.replace(/_/g, ' ')}', 'baseline', v.guidance,
  CASE WHEN '${ck}' = 'adult_beginner' THEN v.gender_notes ELSE NULL END
FROM (VALUES\n${cards.map((c) => {
      const guidance = c.scaling[ck] ?? c.scaling.adult_beginner ?? 'Conservative control dose — prioritize alignment and full rest.'
      return `  (${sqlStr(c.slug)}, ${sqlStr(guidance)}, ${sqlStr(c.genderSpecificNotes)})`
    }).join(',\n')}
) AS v(slug, guidance, gender_notes)
JOIN coaching.exercise e ON e.slug = v.slug
WHERE NOT EXISTS (
  SELECT 1 FROM coaching.exercise_scaling_profile sp
  WHERE sp.exercise_id = e.id AND sp.cohort_key = '${ck}'
);

`
  }

  sql += `INSERT INTO coaching.education_content (
  entity_type, entity_key, entity_id, title, short_summary,
  what_it_is, why_it_works, why_it_matters, why_it_goes_here,
  programming_guidance, common_misuse, scaling_guidance
)
SELECT
  'exercise', e.slug, e.id, e.name, e.card_summary,
  e.description,
  v.why_works,
  'Develops position ownership, alignment, and low-speed joint resilience for safer Output and Capacity.',
  v.why_here,
  v.best_placement,
  v.misuse,
  v.scale_guidance
FROM (VALUES\n${cards.map((c) =>
    `  (${sqlStr(c.slug)}, ${sqlStr(c.whyItWorks)}, ${sqlStr(c.whyItGoesHere)}, ${sqlStr(c.bestPlacement ?? '')}, ${sqlStr(c.commonMisuse)}, ${sqlStr(c.scalingGuidance)})`,
  ).join(',\n')}
) AS v(slug, why_works, why_here, best_placement, misuse, scale_guidance)
JOIN coaching.exercise e ON e.slug = v.slug
ON CONFLICT (entity_type, entity_key, entity_id) DO UPDATE SET
  title = EXCLUDED.title,
  short_summary = EXCLUDED.short_summary,
  what_it_is = EXCLUDED.what_it_is,
  why_it_works = EXCLUDED.why_it_works,
  why_it_goes_here = EXCLUDED.why_it_goes_here,
  programming_guidance = EXCLUDED.programming_guidance,
  common_misuse = EXCLUDED.common_misuse,
  scaling_guidance = EXCLUDED.scaling_guidance,
  updated_at = now();
`

  return sql
}

for (const cluster of CLUSTERS) {
  const sql = generateClusterSql(cluster)
  const outPath = path.join(__dirname, `../backend/migrations/${cluster.file}`)
  fs.writeFileSync(outPath, sql)
  console.log('Wrote', outPath, '—', cluster.cards.length, 'cards')
}
