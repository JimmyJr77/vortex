#!/usr/bin/env node
/**
 * Expert youth-athletics difficulty review — load (weight) + technical (skill/workout).
 *
 *   node scripts/review-exercise-difficulty.mjs
 *   node scripts/review-exercise-difficulty.mjs --dry-run
 *   node scripts/review-exercise-difficulty.mjs --migration-only
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { createRequire } from 'module'
import dotenv from 'dotenv'
import {
  reviewExerciseDifficulty,
  EXERCISE_DIFFICULTY_OVERRIDES,
} from '../backend/platform/exerciseDifficultyReview.js'

const require = createRequire(import.meta.url)
const pg = require('../backend/node_modules/pg')

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')

dotenv.config({ path: path.join(root, 'backend', '.env.local') })
dotenv.config({ path: path.join(root, '.env.local') })

const dryRun = process.argv.includes('--dry-run')
const migrationOnly = process.argv.includes('--migration-only')

function resolveSsl(connectionString) {
  if (process.env.DATABASE_SSL === 'false') return false
  return { rejectUnauthorized: false }
}

function sqlLiteral(value) {
  if (value == null) return 'NULL'
  if (typeof value === 'number') return String(value)
  return `'${String(value).replace(/'/g, "''")}'`
}

function buildMigrationSql(reviews) {
  const lines = [
    '-- Load + technical difficulty review v3 (overall = max(load, technical)).',
    '-- See docs/EXERCISE_DIFFICULTY_METHODOLOGY.md',
    '-- Skill drills: no recommended_age_min. Exercises: age-gated by overall.',
    '-- Generated by scripts/review-exercise-difficulty.mjs',
    '',
  ]

  for (const row of reviews) {
    const d = row.difficulty
    const kind = d.programming_kind ?? 'exercise'
    lines.push(`-- ${row.slug} [${kind}]: ${row.name}`)
    lines.push(`UPDATE coaching.exercise SET programming_kind = ${sqlLiteral(kind)}, updated_at = now() WHERE slug = ${sqlLiteral(row.slug)};`)
    lines.push(`INSERT INTO coaching.exercise_difficulty_profile (
  exercise_id, technical, load, overall,
  recommended_age_min, recommended_age_max, attention_demand, notes, source, updated_at
)
SELECT e.id, ${d.technical}, ${d.load}, ${d.overall},
  ${sqlLiteral(d.recommended_age_min)}, ${sqlLiteral(d.recommended_age_max)}, ${sqlLiteral(d.attention_demand)}, ${sqlLiteral(d.notes)}, 'reviewed', now()
FROM coaching.exercise e
WHERE e.slug = ${sqlLiteral(row.slug)}
ON CONFLICT (exercise_id) DO UPDATE SET
  technical = EXCLUDED.technical,
  load = EXCLUDED.load,
  overall = EXCLUDED.overall,
  recommended_age_min = EXCLUDED.recommended_age_min,
  recommended_age_max = EXCLUDED.recommended_age_max,
  attention_demand = EXCLUDED.attention_demand,
  notes = EXCLUDED.notes,
  source = EXCLUDED.source,
  updated_at = now();`)
    lines.push('')
  }

  return lines.join('\n')
}

async function main() {
  const connectionString = process.env.DATABASE_URL || process.env.DB_URL || process.env.INTERNAL_DB_URL
  if (!connectionString) {
    console.error('DATABASE_URL / DB_URL required')
    process.exit(1)
  }

  const pool = new pg.Pool({ connectionString, ssl: resolveSsl(connectionString) })

  const result = await pool.query(`
    SELECT DISTINCT ON (e.id)
           e.id, e.slug, e.name, e.primary_phase_key, e.phase_subrole, e.skill_level,
           e.participant_structure, e.age_min, e.age_max, e.movement_requirements,
           e.movement_family, e.load_note, e.programming_kind,
           p.technical_complexity, p.fatigue_cost, p.impact_level,
           s.impact_level AS safety_impact, s.minimum_age_recommended,
           r.counts_as_high_intensity, r.counts_as_high_impact,
           e.movement_requirements->>'coordination_demand' AS coord,
           e.movement_requirements->>'impact_level' AS impact_req,
           e.movement_requirements->>'balance_demand' AS balance
    FROM coaching.exercise e
    LEFT JOIN coaching.exercise_phase_profile p ON p.exercise_id = e.id AND p.role = 'primary'
    LEFT JOIN coaching.exercise_safety_profile s ON s.exercise_id = e.id
    LEFT JOIN coaching.exercise_regimen_rule r ON r.exercise_id = e.id
    WHERE e.archived = FALSE
    ORDER BY e.id, p.fit_weight DESC NULLS LAST
  `)

  const reviews = result.rows.map((row) => {
    const override = EXERCISE_DIFFICULTY_OVERRIDES[row.slug] ?? null
    const difficulty = reviewExerciseDifficulty(row, override)
    return { ...row, difficulty }
  })

  const kindCounts = { exercise: 0, skill_drill: 0 }
  const csvLines = ['slug,name,programming_kind,technical,load,overall,recommended_age_min,attention_demand,source,notes']
  for (const row of reviews) {
    const d = row.difficulty
    kindCounts[d.programming_kind] = (kindCounts[d.programming_kind] ?? 0) + 1
    csvLines.push([
      row.slug,
      `"${String(row.name).replace(/"/g, '""')}"`,
      d.programming_kind,
      d.technical,
      d.load,
      d.overall,
      d.recommended_age_min ?? '',
      d.attention_demand,
      d.source,
      `"${String(d.notes).replace(/"/g, '""')}"`,
    ].join(','))
  }

  const migrationPath = path.join(root, 'backend/migrations/216_coaching_exercise_difficulty_v3_reviewed.sql')
  const csvPath = path.join(root, 'docs/exercise-difficulty-review.csv')
  fs.writeFileSync(migrationPath, buildMigrationSql(reviews))
  fs.writeFileSync(csvPath, csvLines.join('\n') + '\n')

  const distribution = {}
  for (const row of reviews) {
    const bucket = row.difficulty.overall
    distribution[bucket] = (distribution[bucket] ?? 0) + 1
  }

  console.log(`Reviewed ${reviews.length} library items`)
  console.log('Programming kind:', kindCounts)
  console.log('Overall distribution:', distribution)
  console.log(`Migration: ${migrationPath}`)
  console.log(`CSV: ${csvPath}`)

  if (dryRun || migrationOnly) {
    await pool.end()
    return
  }

  let upserted = 0
  for (const row of reviews) {
    const d = row.difficulty
    await pool.query(
      `UPDATE coaching.exercise SET programming_kind = $2, updated_at = now() WHERE id = $1`,
      [row.id, d.programming_kind],
    )
    await pool.query(
      `
        INSERT INTO coaching.exercise_difficulty_profile (
          exercise_id, technical, load, overall,
          recommended_age_min, recommended_age_max, attention_demand, notes, source, updated_at
        ) VALUES ($1,$2,$3,$4,$5,$6,$7,$8,$9,now())
        ON CONFLICT (exercise_id) DO UPDATE SET
          technical = EXCLUDED.technical,
          load = EXCLUDED.load,
          overall = EXCLUDED.overall,
          recommended_age_min = EXCLUDED.recommended_age_min,
          recommended_age_max = EXCLUDED.recommended_age_max,
          attention_demand = EXCLUDED.attention_demand,
          notes = EXCLUDED.notes,
          source = EXCLUDED.source,
          updated_at = now()
      `,
      [
        row.id,
        d.technical,
        d.load,
        d.overall,
        d.recommended_age_min,
        d.recommended_age_max,
        d.attention_demand,
        d.notes,
        d.source,
      ],
    )
    upserted++
  }

  console.log(`Upserted ${upserted} profiles and programming_kind values`)
  await pool.end()
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
