/**
 * Generates migration 135 — clone canonical coaching card library to every facility.
 * Run: node scripts/generate-135-facility-backfill.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const outPath = path.join(__dirname, '../backend/migrations/135_coaching_library_facility_backfill.sql')

const PHASE_KEYS = [
  'prepare_access',
  'skill_movement_intelligence',
  'output',
  'capacity',
  'control_resilience',
]

const sql = `-- Multi-facility coaching card library backfill.
-- IDEMPOTENT. Generated by scripts/generate-135-facility-backfill.mjs
-- Seeds historically inserted only into the first facility row; this clones the
-- canonical 250-card library (5 phases × 50) to every facility missing rows.

WITH canonical AS (
  SELECT id AS facility_id FROM public.facility ORDER BY id LIMIT 1
),
phase_keys AS (
  SELECT unnest(ARRAY[${PHASE_KEYS.map((k) => `'${k}'`).join(', ')}]::text[]) AS phase_key
)
INSERT INTO coaching.exercise (
  facility_id, name, slug, description, instructions, sport_id, skill_level, age_min, age_max,
  default_sets, default_reps, default_work_seconds, default_rest_seconds, tempo, load_note,
  est_seconds_per_set, is_published, visibility, archived,
  card_summary, coach_language, athlete_language, programming_logic, why_publish_ready, scalable_variables,
  movement_family, primary_phase_key, phase_subrole, primary_order_slot,
  movement_requirements, coaching_execution, pairing_logic, media_library
)
SELECT
  f.id,
  src.name, src.slug, src.description, src.instructions, src.sport_id, src.skill_level, src.age_min, src.age_max,
  src.default_sets, src.default_reps, src.default_work_seconds, src.default_rest_seconds, src.tempo, src.load_note,
  src.est_seconds_per_set, src.is_published, src.visibility, FALSE,
  src.card_summary, src.coach_language, src.athlete_language, src.programming_logic, src.why_publish_ready, src.scalable_variables,
  src.movement_family, src.primary_phase_key, src.phase_subrole, src.primary_order_slot,
  src.movement_requirements, src.coaching_execution, src.pairing_logic, src.media_library
FROM coaching.exercise src
CROSS JOIN public.facility f
CROSS JOIN canonical c
WHERE src.facility_id = c.facility_id
  AND src.archived = FALSE
  AND src.primary_phase_key IN (SELECT phase_key FROM phase_keys)
  AND f.id <> c.facility_id
  AND NOT EXISTS (
    SELECT 1 FROM coaching.exercise tgt
    WHERE tgt.facility_id = f.id AND tgt.slug = src.slug
  );

-- Clone programming rows from canonical exercises to facility clones (match on slug).
WITH canonical AS (
  SELECT id AS facility_id FROM public.facility ORDER BY id LIMIT 1
),
pairs AS (
  SELECT src.id AS src_id, tgt.id AS tgt_id
  FROM coaching.exercise src
  JOIN coaching.exercise tgt ON tgt.slug = src.slug AND tgt.facility_id <> src.facility_id
  JOIN canonical c ON src.facility_id = c.facility_id
  WHERE src.archived = FALSE
    AND src.primary_phase_key IN (${PHASE_KEYS.map((k) => `'${k}'`).join(', ')})
)
INSERT INTO coaching.exercise_tag (exercise_id, facet_type, facet_id, weight)
SELECT p.tgt_id, t.facet_type, t.facet_id, t.weight
FROM pairs p
JOIN coaching.exercise_tag t ON t.exercise_id = p.src_id
ON CONFLICT (exercise_id, facet_type, facet_id) DO NOTHING;

WITH canonical AS (
  SELECT id AS facility_id FROM public.facility ORDER BY id LIMIT 1
),
pairs AS (
  SELECT src.id AS src_id, tgt.id AS tgt_id
  FROM coaching.exercise src
  JOIN coaching.exercise tgt ON tgt.slug = src.slug AND tgt.facility_id <> src.facility_id
  JOIN canonical c ON src.facility_id = c.facility_id
  WHERE src.archived = FALSE
    AND src.primary_phase_key IN (${PHASE_KEYS.map((k) => `'${k}'`).join(', ')})
)
INSERT INTO coaching.exercise_phase_profile (
  exercise_id, phase_id, fit_weight, role, order_slot, order_index,
  freshness_required, fatigue_sensitivity, fatigue_cost, technical_complexity, impact_level, intensity_ceiling, notes
)
SELECT
  p.tgt_id, pp.phase_id, pp.fit_weight, pp.role, pp.order_slot, pp.order_index,
  pp.freshness_required, pp.fatigue_sensitivity, pp.fatigue_cost, pp.technical_complexity, pp.impact_level, pp.intensity_ceiling, pp.notes
FROM pairs p
JOIN coaching.exercise_phase_profile pp ON pp.exercise_id = p.src_id
ON CONFLICT (exercise_id, phase_id) DO UPDATE SET
  role = EXCLUDED.role,
  order_slot = EXCLUDED.order_slot,
  order_index = EXCLUDED.order_index,
  fit_weight = EXCLUDED.fit_weight,
  freshness_required = EXCLUDED.freshness_required,
  fatigue_sensitivity = EXCLUDED.fatigue_sensitivity,
  fatigue_cost = EXCLUDED.fatigue_cost,
  technical_complexity = EXCLUDED.technical_complexity,
  impact_level = EXCLUDED.impact_level,
  intensity_ceiling = EXCLUDED.intensity_ceiling,
  notes = EXCLUDED.notes;

WITH canonical AS (
  SELECT id AS facility_id FROM public.facility ORDER BY id LIMIT 1
),
pairs AS (
  SELECT src.id AS src_id, tgt.id AS tgt_id
  FROM coaching.exercise src
  JOIN coaching.exercise tgt ON tgt.slug = src.slug AND tgt.facility_id <> src.facility_id
  JOIN canonical c ON src.facility_id = c.facility_id
  WHERE src.archived = FALSE
    AND src.primary_phase_key IN (${PHASE_KEYS.map((k) => `'${k}'`).join(', ')})
)
INSERT INTO coaching.exercise_dosage_profile (
  exercise_id, profile_name, is_default, volume_unit,
  default_sets, default_reps, default_work_seconds, default_distance, default_contacts, default_rounds,
  default_rest_seconds, tempo, load_type, default_intensity, default_rpe_min, default_rpe_max, default_load_note,
  est_seconds_per_set, session_volume_min, session_volume_max, weekly_volume_min, weekly_volume_max
)
SELECT
  p.tgt_id, d.profile_name, d.is_default, d.volume_unit,
  d.default_sets, d.default_reps, d.default_work_seconds, d.default_distance, d.default_contacts, d.default_rounds,
  d.default_rest_seconds, d.tempo, d.load_type, d.default_intensity, d.default_rpe_min, d.default_rpe_max, d.default_load_note,
  d.est_seconds_per_set, d.session_volume_min, d.session_volume_max, d.weekly_volume_min, d.weekly_volume_max
FROM pairs p
JOIN coaching.exercise_dosage_profile d ON d.exercise_id = p.src_id
ON CONFLICT (exercise_id, profile_name) DO UPDATE SET
  is_default = EXCLUDED.is_default,
  volume_unit = EXCLUDED.volume_unit,
  default_sets = EXCLUDED.default_sets,
  default_reps = EXCLUDED.default_reps,
  default_work_seconds = EXCLUDED.default_work_seconds,
  default_rest_seconds = EXCLUDED.default_rest_seconds,
  est_seconds_per_set = EXCLUDED.est_seconds_per_set,
  session_volume_min = EXCLUDED.session_volume_min,
  session_volume_max = EXCLUDED.session_volume_max,
  weekly_volume_min = EXCLUDED.weekly_volume_min,
  weekly_volume_max = EXCLUDED.weekly_volume_max;

WITH canonical AS (
  SELECT id AS facility_id FROM public.facility ORDER BY id LIMIT 1
),
pairs AS (
  SELECT src.id AS src_id, tgt.id AS tgt_id
  FROM coaching.exercise src
  JOIN coaching.exercise tgt ON tgt.slug = src.slug AND tgt.facility_id <> src.facility_id
  JOIN canonical c ON src.facility_id = c.facility_id
  WHERE src.archived = FALSE
    AND src.primary_phase_key IN (${PHASE_KEYS.map((k) => `'${k}'`).join(', ')})
)
INSERT INTO coaching.exercise_safety_profile (
  exercise_id, risk_level, impact_level, requires_spotting, requires_coach_supervision,
  minimum_age_recommended, minimum_skill_level, minimum_prerequisite_notes,
  readiness_checks, stop_signs, common_substitutions
)
SELECT
  p.tgt_id, s.risk_level, s.impact_level, s.requires_spotting, s.requires_coach_supervision,
  s.minimum_age_recommended, s.minimum_skill_level, s.minimum_prerequisite_notes,
  s.readiness_checks, s.stop_signs, s.common_substitutions
FROM pairs p
JOIN coaching.exercise_safety_profile s ON s.exercise_id = p.src_id
ON CONFLICT (exercise_id) DO UPDATE SET
  risk_level = EXCLUDED.risk_level,
  impact_level = EXCLUDED.impact_level,
  requires_spotting = EXCLUDED.requires_spotting,
  requires_coach_supervision = EXCLUDED.requires_coach_supervision,
  minimum_age_recommended = EXCLUDED.minimum_age_recommended,
  minimum_skill_level = EXCLUDED.minimum_skill_level,
  minimum_prerequisite_notes = EXCLUDED.minimum_prerequisite_notes,
  readiness_checks = EXCLUDED.readiness_checks,
  stop_signs = EXCLUDED.stop_signs,
  common_substitutions = EXCLUDED.common_substitutions;

WITH canonical AS (
  SELECT id AS facility_id FROM public.facility ORDER BY id LIMIT 1
),
pairs AS (
  SELECT src.id AS src_id, tgt.id AS tgt_id
  FROM coaching.exercise src
  JOIN coaching.exercise tgt ON tgt.slug = src.slug AND tgt.facility_id <> src.facility_id
  JOIN canonical c ON src.facility_id = c.facility_id
  WHERE src.archived = FALSE
    AND src.primary_phase_key IN (${PHASE_KEYS.map((k) => `'${k}'`).join(', ')})
)
INSERT INTO coaching.exercise_regimen_rule (
  exercise_id, can_be_daily, daily_min_dose, daily_max_dose, weekly_min_frequency, weekly_max_frequency,
  minimum_hours_between_hard_exposures, counts_as_high_intensity, counts_as_high_impact, counts_as_neural,
  counts_as_tissue_stress, counts_as_conditioning, recovery_notes
)
SELECT
  p.tgt_id, r.can_be_daily, r.daily_min_dose, r.daily_max_dose, r.weekly_min_frequency, r.weekly_max_frequency,
  r.minimum_hours_between_hard_exposures, r.counts_as_high_intensity, r.counts_as_high_impact, r.counts_as_neural,
  r.counts_as_tissue_stress, r.counts_as_conditioning, r.recovery_notes
FROM pairs p
JOIN coaching.exercise_regimen_rule r ON r.exercise_id = p.src_id
ON CONFLICT (exercise_id) DO UPDATE SET
  can_be_daily = EXCLUDED.can_be_daily,
  daily_min_dose = EXCLUDED.daily_min_dose,
  daily_max_dose = EXCLUDED.daily_max_dose,
  weekly_min_frequency = EXCLUDED.weekly_min_frequency,
  weekly_max_frequency = EXCLUDED.weekly_max_frequency,
  minimum_hours_between_hard_exposures = EXCLUDED.minimum_hours_between_hard_exposures,
  counts_as_high_intensity = EXCLUDED.counts_as_high_intensity,
  counts_as_high_impact = EXCLUDED.counts_as_high_impact,
  counts_as_neural = EXCLUDED.counts_as_neural,
  counts_as_tissue_stress = EXCLUDED.counts_as_tissue_stress,
  counts_as_conditioning = EXCLUDED.counts_as_conditioning,
  recovery_notes = EXCLUDED.recovery_notes;

WITH canonical AS (
  SELECT id AS facility_id FROM public.facility ORDER BY id LIMIT 1
),
pairs AS (
  SELECT src.id AS src_id, tgt.id AS tgt_id
  FROM coaching.exercise src
  JOIN coaching.exercise tgt ON tgt.slug = src.slug AND tgt.facility_id <> src.facility_id
  JOIN canonical c ON src.facility_id = c.facility_id
  WHERE src.archived = FALSE
    AND src.primary_phase_key IN (${PHASE_KEYS.map((k) => `'${k}'`).join(', ')})
)
INSERT INTO coaching.exercise_scaling_profile (
  exercise_id, label, age_min, age_max, skill_level, training_age_min_months, training_age_max_months,
  scale_direction, load_guidance, complexity_guidance, coach_notes, cohort_key
)
SELECT
  p.tgt_id, s.label, s.age_min, s.age_max, s.skill_level, s.training_age_min_months, s.training_age_max_months,
  s.scale_direction, s.load_guidance, s.complexity_guidance, s.coach_notes, s.cohort_key
FROM pairs p
JOIN coaching.exercise_scaling_profile s ON s.exercise_id = p.src_id
WHERE NOT EXISTS (
  SELECT 1 FROM coaching.exercise_scaling_profile existing
  WHERE existing.exercise_id = p.tgt_id
    AND COALESCE(existing.cohort_key, '') = COALESCE(s.cohort_key, '')
    AND existing.label = s.label
);
`

fs.writeFileSync(outPath, sql)
console.log(`Wrote ${outPath}`)
