/**
 * Generates backend/migrations/141_coaching_programming_library_seed.sql
 * Run: node scripts/generate-141-programming-seed.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { PROGRAMMING_METHODS } from './data/programming-methods-top50.mjs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const OUT = path.join(__dirname, '../backend/migrations/141_coaching_programming_library_seed.sql')
const CANONICAL_FACILITY_SQL = '(SELECT id FROM public.facility ORDER BY id LIMIT 1)'

function sqlStr(s) {
  if (s == null) return 'NULL'
  return `'${String(s).replace(/'/g, "''")}'`
}

function sqlTextArray(arr) {
  if (!arr?.length) return "'{}'"
  return `ARRAY[${arr.map((v) => sqlStr(v)).join(', ')}]`
}

function sqlJson(obj) {
  return `'${JSON.stringify(obj ?? {}).replace(/'/g, "''")}'::jsonb`
}

function slugList(methods) {
  return methods.map((m) => sqlStr(m.slug)).join(', ')
}

let sql = `-- Seed 50 Programming Library method cards (canonical facility).
-- IDEMPOTENT. Generated by scripts/generate-141-programming-seed.mjs

INSERT INTO coaching.programming_method (
  facility_id, name, slug, category, definition, coach_summary, athlete_summary,
  primary_development_goal, secondary_development_goals, programming_type,
  best_session_phase, compatible_session_phases, incompatible_phases,
  energy_system_focus, fatigue_profile, supervision_level,
  what_it_is, why_it_matters, when_to_use, when_not_to_use, common_misuse,
  work_rest_structure, exercise_compatibility, workout_builder_rules,
  is_published, visibility
)
SELECT
  ${CANONICAL_FACILITY_SQL},
  d.name, d.slug, d.category, d.definition, d.coach_summary, d.athlete_summary,
  d.primary_goal, d.secondary_goals, d.programming_type,
  d.best_phase, d.compatible_phases, d.incompatible_phases,
  d.energy_systems, d.fatigue_profile::jsonb, d.supervision_level,
  d.what_it_is, d.why_it_matters, d.when_to_use, d.when_not_to_use, d.common_misuse,
  d.work_rest::jsonb, d.exercise_compat::jsonb, d.builder_rules::jsonb,
  TRUE, 'facility'
FROM (VALUES
`

sql += PROGRAMMING_METHODS.map((m) => {
  return `  (${sqlStr(m.name)}, ${sqlStr(m.slug)}, ${sqlStr(m.category)}, ${sqlStr(m.definition)}, ${sqlStr(m.coach_summary)}, ${sqlStr(m.athlete_summary)}, ${sqlStr(m.primary_development_goal)}, ${sqlTextArray(m.secondary_development_goals)}, ${sqlStr(m.programming_type)}, ${sqlStr(m.best_session_phase)}, ${sqlTextArray(m.compatible_session_phases)}, ${sqlTextArray(m.incompatible_phases)}, ${sqlTextArray(m.energy_system_focus)}, ${sqlJson(m.fatigue_profile).replace('::jsonb', '')}, ${sqlStr(m.supervision_level)}, ${sqlStr(m.what_it_is)}, ${sqlStr(m.why_it_matters)}, ${sqlStr(m.when_to_use)}, ${sqlStr(m.when_not_to_use)}, ${sqlTextArray(m.common_misuse)}, ${sqlJson(m.work_rest_structure).replace('::jsonb', '')}, ${sqlJson(m.exercise_compatibility).replace('::jsonb', '')}, ${sqlJson(m.workout_builder_rules).replace('::jsonb', '')})`
}).join(',\n')

sql += `
) AS d(
  name, slug, category, definition, coach_summary, athlete_summary,
  primary_goal, secondary_goals, programming_type,
  best_phase, compatible_phases, incompatible_phases,
  energy_systems, fatigue_profile, supervision_level,
  what_it_is, why_it_matters, when_to_use, when_not_to_use, common_misuse,
  work_rest, exercise_compat, builder_rules
)
ON CONFLICT (facility_id, slug) DO UPDATE SET
  name = EXCLUDED.name,
  category = EXCLUDED.category,
  definition = EXCLUDED.definition,
  coach_summary = EXCLUDED.coach_summary,
  athlete_summary = EXCLUDED.athlete_summary,
  primary_development_goal = EXCLUDED.primary_development_goal,
  secondary_development_goals = EXCLUDED.secondary_development_goals,
  programming_type = EXCLUDED.programming_type,
  best_session_phase = EXCLUDED.best_session_phase,
  compatible_session_phases = EXCLUDED.compatible_session_phases,
  incompatible_phases = EXCLUDED.incompatible_phases,
  energy_system_focus = EXCLUDED.energy_system_focus,
  fatigue_profile = EXCLUDED.fatigue_profile,
  supervision_level = EXCLUDED.supervision_level,
  what_it_is = EXCLUDED.what_it_is,
  why_it_matters = EXCLUDED.why_it_matters,
  when_to_use = EXCLUDED.when_to_use,
  when_not_to_use = EXCLUDED.when_not_to_use,
  common_misuse = EXCLUDED.common_misuse,
  work_rest_structure = EXCLUDED.work_rest_structure,
  exercise_compatibility = EXCLUDED.exercise_compatibility,
  workout_builder_rules = EXCLUDED.workout_builder_rules,
  updated_at = now();

`

const childTables = [
  'programming_method_phase_profile',
  'programming_method_prescription_profile',
  'programming_method_exercise_compatibility',
  'programming_method_quality_standard',
  'programming_method_stop_rule',
  'programming_method_validator_rule',
  'programming_method_example',
]

for (const table of childTables) {
  sql += `DELETE FROM coaching.${table}
WHERE programming_method_id IN (
  SELECT id FROM coaching.programming_method
  WHERE facility_id = ${CANONICAL_FACILITY_SQL} AND slug IN (${slugList(PROGRAMMING_METHODS)})
);

`
}

sql += `INSERT INTO coaching.programming_method_phase_profile (
  programming_method_id, phase_key, role, fit_weight
)
SELECT pm.id, v.phase_key, v.role, v.fit_weight
FROM (VALUES
`
const phaseRows = []
for (const m of PROGRAMMING_METHODS) {
  for (const pp of m.phase_profiles ?? []) {
    phaseRows.push(`  (${sqlStr(m.slug)}, ${sqlStr(pp.phase_key)}, ${sqlStr(pp.role)}, ${pp.fit_weight})`)
  }
}
sql += phaseRows.join(',\n')
sql += `
) AS v(slug, phase_key, role, fit_weight)
JOIN coaching.programming_method pm ON pm.slug = v.slug AND pm.facility_id = ${CANONICAL_FACILITY_SQL}
ON CONFLICT (programming_method_id, phase_key) DO UPDATE SET
  role = EXCLUDED.role,
  fit_weight = EXCLUDED.fit_weight;

`

sql += `INSERT INTO coaching.programming_method_prescription_profile (
  programming_method_id, profile_name,
  default_total_minutes, default_rounds, default_work_seconds, default_rest_seconds,
  default_rpe_min, default_rpe_max, notes
)
SELECT pm.id, v.profile_name,
  v.total_minutes, v.rounds, v.work_seconds, v.rest_seconds,
  v.rpe_min, v.rpe_max, v.notes
FROM (VALUES
`
const rxRows = []
for (const m of PROGRAMMING_METHODS) {
  for (const rx of m.prescriptions ?? []) {
    rxRows.push(`  (${sqlStr(m.slug)}, ${sqlStr(rx.profile_name)}, ${rx.default_total_minutes ?? 'NULL'}, ${rx.default_rounds ?? 'NULL'}, ${rx.default_work_seconds ?? 'NULL'}, ${rx.default_rest_seconds ?? 'NULL'}, ${rx.default_rpe_min ?? 'NULL'}, ${rx.default_rpe_max ?? 'NULL'}, ${sqlStr(rx.notes ?? '')})`)
  }
}
sql += rxRows.join(',\n')
sql += `
) AS v(slug, profile_name, total_minutes, rounds, work_seconds, rest_seconds, rpe_min, rpe_max, notes)
JOIN coaching.programming_method pm ON pm.slug = v.slug AND pm.facility_id = ${CANONICAL_FACILITY_SQL}
ON CONFLICT (programming_method_id, profile_name) DO UPDATE SET
  default_total_minutes = EXCLUDED.default_total_minutes,
  default_rounds = EXCLUDED.default_rounds,
  default_work_seconds = EXCLUDED.default_work_seconds,
  default_rest_seconds = EXCLUDED.default_rest_seconds,
  default_rpe_min = EXCLUDED.default_rpe_min,
  default_rpe_max = EXCLUDED.default_rpe_max,
  notes = EXCLUDED.notes;

`

sql += `INSERT INTO coaching.programming_method_exercise_compatibility (
  programming_method_id, compatibility_type, exercise_type, weight, rationale
)
SELECT pm.id, v.compat_type, v.exercise_type, v.weight, v.rationale
FROM (VALUES
`
const compatRows = []
for (const m of PROGRAMMING_METHODS) {
  const ec = m.exercise_compatibility ?? {}
  for (const t of ec.compatible_exercise_types ?? []) {
    compatRows.push(`  (${sqlStr(m.slug)}, 'compatible', ${sqlStr(t)}, 4, ${sqlStr('Compatible with this programming method.')})`)
  }
  for (const t of ec.avoid_exercise_types ?? []) {
    compatRows.push(`  (${sqlStr(m.slug)}, 'avoid', ${sqlStr(t)}, 5, ${sqlStr('Avoid under this programming method.')})`)
  }
  for (const t of ec.conditional_exercise_types ?? []) {
    compatRows.push(`  (${sqlStr(m.slug)}, 'conditional', ${sqlStr(t)}, 3, ${sqlStr('Use only when load, skill, and supervision support it.')})`)
  }
}
sql += compatRows.join(',\n')
sql += `
) AS v(slug, compat_type, exercise_type, weight, rationale)
JOIN coaching.programming_method pm ON pm.slug = v.slug AND pm.facility_id = ${CANONICAL_FACILITY_SQL};

`

sql += `INSERT INTO coaching.programming_method_quality_standard (
  programming_method_id, standard, severity
)
SELECT pm.id, v.standard, v.severity
FROM (VALUES
`
const qsRows = []
for (const m of PROGRAMMING_METHODS) {
  for (const standard of m.quality_standards ?? []) {
    qsRows.push(`  (${sqlStr(m.slug)}, ${sqlStr(standard)}, 'required')`)
  }
}
sql += qsRows.join(',\n')
sql += `
) AS v(slug, standard, severity)
JOIN coaching.programming_method pm ON pm.slug = v.slug AND pm.facility_id = ${CANONICAL_FACILITY_SQL};

`

sql += `INSERT INTO coaching.programming_method_stop_rule (
  programming_method_id, stop_rule, severity
)
SELECT pm.id, v.stop_rule, v.severity
FROM (VALUES
`
const stopRows = []
for (const m of PROGRAMMING_METHODS) {
  for (const rule of m.stop_rules ?? []) {
    stopRows.push(`  (${sqlStr(m.slug)}, ${sqlStr(rule)}, 'stop')`)
  }
}
sql += stopRows.join(',\n')
sql += `
) AS v(slug, stop_rule, severity)
JOIN coaching.programming_method pm ON pm.slug = v.slug AND pm.facility_id = ${CANONICAL_FACILITY_SQL};

`

sql += `INSERT INTO coaching.programming_method_validator_rule (
  programming_method_id, rule_key, condition_json, message, severity
)
SELECT pm.id, v.rule_key, v.condition_json::jsonb, v.message, v.severity
FROM (VALUES
`
const valRows = []
for (const m of PROGRAMMING_METHODS) {
  for (const vr of m.validator_rules ?? []) {
    valRows.push(`  (${sqlStr(m.slug)}, ${sqlStr(vr.rule_key)}, ${sqlJson(vr.condition_json ?? {}).replace('::jsonb', '')}, ${sqlStr(vr.message)}, ${sqlStr(vr.severity)})`)
  }
}
sql += valRows.join(',\n')
sql += `
) AS v(slug, rule_key, condition_json, message, severity)
JOIN coaching.programming_method pm ON pm.slug = v.slug AND pm.facility_id = ${CANONICAL_FACILITY_SQL};

`

sql += `INSERT INTO coaching.programming_method_example (
  programming_method_id, label, audience, example_json, coaching_notes
)
SELECT pm.id, v.label, v.audience, v.example_json::jsonb, v.coaching_notes
FROM (VALUES
`
const exRows = []
for (const m of PROGRAMMING_METHODS) {
  for (const ex of m.examples ?? []) {
    exRows.push(`  (${sqlStr(m.slug)}, ${sqlStr(ex.label)}, ${sqlStr(ex.audience)}, ${sqlJson(ex.example_json ?? {}).replace('::jsonb', '')}, ${sqlStr(ex.coaching_notes ?? '')})`)
  }
}
sql += exRows.join(',\n')
sql += `
) AS v(slug, label, audience, example_json, coaching_notes)
JOIN coaching.programming_method pm ON pm.slug = v.slug AND pm.facility_id = ${CANONICAL_FACILITY_SQL};

`

fs.writeFileSync(OUT, sql)
console.log(`Wrote ${OUT} (${PROGRAMMING_METHODS.length} methods)`)
